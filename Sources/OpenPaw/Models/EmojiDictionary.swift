    static func search(_ query: String, limit: Int = 8) -> [EmojiEntry] {
        let q = query.lowercased()
        guard q.count >= 2 else { return [] }

        // Exact prefix matches first, then contains
        var prefixMatches: [EmojiEntry] = []
        var containsMatches: [EmojiEntry] = []

        for entry in all {
            if prefixMatches.count + containsMatches.count >= limit * 2 { break }
            let hasPrefix = entry.keywords.contains { $0.hasPrefix(q) }
            let hasContains = !hasPrefix && entry.keywords.contains { $0.contains(q) }
            if hasPrefix { prefixMatches.append(entry) }
            else if hasContains { containsMatches.append(entry) }
        }

        return Array((prefixMatches + containsMatches).prefix(limit))
    }

